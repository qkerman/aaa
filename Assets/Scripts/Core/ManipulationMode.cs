namespace EVA
{
    /// <summary>
    /// This enumeration helps to manages the manipulation mode of an object.
    /// </summary>
    public enum ManipulationMode
    {
        /// <summary>
        /// Value for translation mode.
        /// </summary>
        TRANSLATION = 0,
        /// <summary>
        /// Value for rotation mode.
        /// </summary>
        ROTATION = 1,
        /// <summary>
        /// Value for scaling mode.
        /// </summary>
        SCALING = 2
    }
}